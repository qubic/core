    bool saveSnapshot(unsigned short epoch, CHAR16* directory) const;

    /// Load state from snapshot files. Can only be called from main processor!
    bool loadSnapshot(unsigned short epoch, CHAR16* directory);

    /**
    * Check and start user query based on transaction (should be called from tick processor).
    * @param tx Transaction, whose validity and signature has been checked before.
    * @param txIndex Index of tx in tick data, for referencing in tick storage.
    * @return Query ID or -1 on error.
    */
    int64_t startUserQuery(const OracleUserQueryTransactionPrefix* tx, uint32_t txIndex)
    {
        // check preconditions (that function is used correctly)
        ASSERT(tx);
        ASSERT(tx->checkValidity());
        ASSERT(isZero(tx->destinationPublicKey));
        ASSERT(tx->tick == system.tick);
        ASSERT(txIndex < NUMBER_OF_TRANSACTIONS_PER_TICK);
        {
            // check that tick storage contains tx at expected position
            ASSERT(ts.tickInCurrentEpochStorage(tx->tick));
            const uint64_t* tsTickTransactionOffsets = ts.tickTransactionOffsets.getByTickInCurrentEpoch(tx->tick);
            const auto* tsTx = ts.tickTransactions.ptr(tsTickTransactionOffsets[txIndex]);
            ASSERT(compareMem(tx, tsTx, tx->totalSize()) == 0);
        }

        // check size of tx / query data and validity of interface index
        const uint16_t querySize = tx->inputSize - OracleUserQueryTransactionPrefix::minInputSize();
        if (tx->inputSize < OracleUserQueryTransactionPrefix::minInputSize()
            || tx->oracleInterfaceIndex >= OI::oracleInterfacesCount
            || querySize != OI::oracleInterfaces[tx->oracleInterfaceIndex].querySize)
        {
#if !defined(NDEBUG) && !defined(NO_UEFI)
            addDebugMessage(L"Cannot start user oracle query due to inputSize / oracleInterface issue!");
#endif
            return -1;
        }

        // check fee
        const void* queryData = (tx + 1);
        const int64_t fee = OI::getOracleQueryFeeFunc[tx->oracleInterfaceIndex](queryData);
        if (tx->amount < fee)
        {
            // tx amount insufficient for fee -> return error (caller should refund in all error cases)
#if !defined(NDEBUG) && !defined(NO_UEFI)
            addDebugMessage(L"Cannot start user oracle query due to insufficient invocation reward!");
#endif
            return -1;
        }

        // lock for accessing engine data
        LockGuard lockGuard(lock);

        // compute timeout as absolute point in time
        auto timeout = getTimeoutTimestamp(QPI::DateAndTime::now(), tx->timeoutMilliseconds);
        if (!timeout.isValid())
            return -1;

        // compose query ID
        int64_t queryId = ((int64_t)system.tick << 31) | txIndex;
        ASSERT(txIndex < (1ull << 31));

        // run common part of starting query and init query metadata
        OracleQueryMetadata* oqm = startQuery(queryId, ORACLE_QUERY_TYPE_USER_QUERY, tx->oracleInterfaceIndex, timeout, 0);
        if (!oqm)
            return -1;
        oqm->typeVar.user.queryingEntity = tx->sourcePublicKey;
        oqm->typeVar.user.queryTxIndex = txIndex;

        // enqueue query message to oracle machine node
        enqueueOracleQuery(queryId, tx->oracleInterfaceIndex, tx->timeoutMilliseconds, queryData, querySize);

        // log status change
        logQueryStatusChange(*oqm);

        // Debug logging
#if ENABLE_ORACLE_STATS_RECORD
        oracleStats[oqm->interfaceIndex].queryCount++;
#endif

#if !defined(NDEBUG) && !defined(NO_UEFI)
        CHAR16 dbgMsg[200];
        setText(dbgMsg, L"oracleEngine.startUserQuery(), tick ");
        appendNumber(dbgMsg, system.tick, FALSE);
        appendText(dbgMsg, ", queryId ");
        appendNumber(dbgMsg, queryId, FALSE);
        appendText(dbgMsg, ", interfaceIndex ");
        appendNumber(dbgMsg, tx->oracleInterfaceIndex, FALSE);
        appendText(dbgMsg, ", timeout ");
        appendDateAndTime(dbgMsg, timeout);
        appendText(dbgMsg, ", now ");
        appendDateAndTime(dbgMsg, QPI::DateAndTime::now());
        addDebugMessage(dbgMsg);
#endif

        if (tx->amount > fee)
        {
            // success case: refund the amount that has been paid too much (error case refund is handled by caller)
            refundFees(tx->sourcePublicKey, tx->amount - fee);
        }
        ++stats.userQueries;

        return queryId;
    }

    int64_t startContractQuery(uint16_t contractIndex, uint32_t interfaceIndex,
        const void* queryData, uint16_t querySize, uint32_t timeoutMillisec,
        unsigned int notificationProcId)
    {
        // check inputs
        if (contractIndex >= MAX_NUMBER_OF_CONTRACTS || interfaceIndex >= OI::oracleInterfacesCount || querySize != OI::oracleInterfaces[interfaceIndex].querySize)
        {
#if !defined(NDEBUG) && !defined(NO_UEFI)
            addDebugMessage(L"Cannot start contract oracle query due to contractIndex / oracleInterface issue!");
#endif
            return -1;
        }

        // lock for accessing engine data
        LockGuard lockGuard(lock);

        // compute timeout as absolute point in time
        auto timeout = getTimeoutTimestamp(QPI::DateAndTime::now(), timeoutMillisec);
        if (!timeout.isValid())
            return -1;

        // get new query ID
        int64_t queryId = getNewNonTxQueryId();
        if (queryId < 0)
            return -1;

        // run common part of starting query and init query metadata
        OracleQueryMetadata* oqm = startQuery(queryId, ORACLE_QUERY_TYPE_CONTRACT_QUERY, interfaceIndex, timeout, querySize);
        if (!oqm)
            return -1;
        oqm->typeVar.contract.queryingContract = contractIndex;
        oqm->typeVar.contract.queryStorageOffset = queryStorageBytesUsed;
        oqm->typeVar.contract.notificationProcId = notificationProcId;

        // copy oracle query data to permanent storage
        copyMem(queryStorage + queryStorageBytesUsed, queryData, querySize);
        queryStorageBytesUsed += querySize;

        // enqueue query message to oracle machine node
        enqueueOracleQuery(queryId, interfaceIndex, timeoutMillisec, queryData, querySize);

        // log status change
        logQueryStatusChange(*oqm);

        // Debug logging
#if ENABLE_ORACLE_STATS_RECORD
        oracleStats[oqm->interfaceIndex].queryCount++;
#endif

#if !defined(NDEBUG) && !defined(NO_UEFI)
        CHAR16 dbgMsg[200];
        setText(dbgMsg, L"oracleEngine.startContractQuery(), tick ");
        appendNumber(dbgMsg, system.tick, FALSE);
        appendText(dbgMsg, ", queryId ");
        appendNumber(dbgMsg, queryId, FALSE);
        appendText(dbgMsg, ", interfaceIndex ");
        appendNumber(dbgMsg, interfaceIndex, FALSE);
        appendText(dbgMsg, ", timeout ");
        appendDateAndTime(dbgMsg, timeout);
        appendText(dbgMsg, ", now ");
        appendDateAndTime(dbgMsg, QPI::DateAndTime::now());
        addDebugMessage(dbgMsg);
#endif

        ++stats.contractQueries;

        return queryId;
    }

    int32_t startContractSubscription(uint16_t contractIndex, uint32_t interfaceIndex,
        const void* queryData, uint16_t querySize, uint32_t notificationPeriodMillisec,
        unsigned int notificationProcId, uint16_t timestampOffsetInQuery)
    {
        // check inputs
        if (contractIndex >= MAX_NUMBER_OF_CONTRACTS || interfaceIndex >= OI::oracleInterfacesCount
            || querySize != OI::oracleInterfaces[interfaceIndex].querySize
            || timestampOffsetInQuery + sizeof(QPI::DateAndTime) > querySize)
        {
#if !defined(NDEBUG) && !defined(NO_UEFI)
            addDebugMessage(L"Cannot start contract oracle subscription due to contractIndex / oracleInterface / timestampOffsetInQuery issue!");
#endif
            return -1;
        }

        // convert notification period
        const uint32_t _notificationPeriodMinutes = notificationPeriodMillisec / 60000;
        if (notificationPeriodMillisec % 60000 != 0 || _notificationPeriodMinutes < 1 || _notificationPeriodMinutes > 1440)
        {
#if !defined(NDEBUG) && !defined(NO_UEFI)
            addDebugMessage(L"Cannot start contract oracle subscription due to invalid notification period!");
#endif
            return -1;
        }
        const uint16_t notificationPeriodMinutes = uint16_t(_notificationPeriodMinutes);

        // lock for accessing engine data
        LockGuard lockGuard(lock);

        // search existing subscriptions for same interface/query
        int32_t subscriptionId = -1;
        {
            const uint16_t cmpSize1 = timestampOffsetInQuery;
            const uint16_t cmpOffset2 = timestampOffsetInQuery + sizeof(QPI::DateAndTime);
            const uint16_t cmpSize2 = querySize - cmpOffset2;
            for (int32_t i = 0; i < usedSubscriptionSlots; ++i)
            {
                const OracleSubscription& os = subscriptions[i];
                if (!os.initialQueryStorageOffset)
                    continue;
                if (os.interfaceIndex == interfaceIndex)
                {
                    // check if new query differs from query of existing subscription i
                    // (timestamp is excluded from comparison)
                    const uint8_t* queryDataStorage = queryStorage + os.initialQueryStorageOffset;
                    if (cmpSize1 && compareMem(queryDataStorage, queryData, cmpSize1) != 0)
                        continue;
                    if (cmpSize2 && compareMem(queryDataStorage + cmpOffset2, ((uint8_t*)queryData) + cmpOffset2, cmpSize2) != 0)
                        continue;

                    // query matches
                    subscriptionId = i;
                    break;
                }
            }
        }

        // check if this contract already has this subscription
        int32_t subscriberIndex = -1;
        if (subscriptionId >= 0)
        {
            const OracleSubscription& subscription = subscriptions[subscriptionId];
            int32_t idx = subscription.firstSubscriberIndex;
            while (idx >= 0)
            {
                const OracleSubscriber& subscriber = subscribers[idx];
                ASSERT(subscriber.subscriptionId == subscriptionId);
                if (subscriber.contractIndex == contractIndex)
                {
                    // Contract already is subscriber of subscription
                    // -> forbid changing a subscription to simplify code
                    // -> error (user needs to unsubscribe and subscribe again)
#if !defined(NDEBUG) && !defined(NO_UEFI)
                    addDebugMessage(L"Cannot start contract oracle subscription because contract is already subscribed!");
#endif
                    return -1;
                }
                idx = subscriber.nextSubscriberIdx;
            }
        }

        // check if we have free slots for creating new records
        ASSERT(usedSubscriptionSlots <= MAX_ORACLE_SUBSCRIPTIONS);
        ASSERT(usedSubscriberSlots <= MAX_ORACLE_SUBSCRIBERS);
        if ((subscriptionId < 0 && (usedSubscriptionSlots >= MAX_ORACLE_SUBSCRIPTIONS
                                    || queryStorageBytesUsed + querySize > ORACLE_QUERY_STORAGE_SIZE
                                    || nextSubscriptionIdQueue.size() >= nextSubscriptionIdQueue.capacity()))
            || (usedSubscriberSlots >= MAX_ORACLE_SUBSCRIBERS))
        {
#if !defined(NDEBUG) && !defined(NO_UEFI)
            addDebugMessage(L"Cannot start contract oracle subscription due to missing space!");
#endif
            return -1;
        }

        // Create subscription record if we haven't found a matching one
        const QPI::DateAndTime now = QPI::DateAndTime::now();
        if (subscriptionId < 0)
        {
            // get subscription ID and init new record
            subscriptionId = usedSubscriptionSlots++;
            OracleSubscription& s = subscriptions[subscriptionId];
            s.initialQueryStorageOffset = queryStorageBytesUsed;
            s.interfaceIndex = interfaceIndex;
            s.queryTimestampOffset = timestampOffsetInQuery;
            s.lastPendingQueryId = -1;
            s.lastRevealedQueryId = -1;
            s.nextQueryTimestamp.setInvalid(/*smallestValue=*/false);
            s.subscriberCount = 0;
            s.firstSubscriberIndex = -1;

            // copy oracle query data to permanent storage
            copyMem(queryStorage + queryStorageBytesUsed, queryData, querySize);
            queryStorageBytesUsed += querySize;
        }
        OracleSubscription& subscription = subscriptions[subscriptionId];

        // Create subscriber record
        subscriberIndex = usedSubscriberSlots++;
        OracleSubscriber& subscriber = subscribers[subscriberIndex];
        subscriber.subscriptionId = subscriptionId;
        subscriber.contractIndex = contractIndex;
        subscriber.notificationPeriodMinutes = notificationPeriodMinutes;
        subscriber.notificationProcId = notificationProcId;
        ++subscription.subscriberCount;

        // Set or update scheduling info
        ASSERT(nextSubscriptionIdQueue.capacity() == MAX_ORACLE_SUBSCRIPTIONS);
        if (subscription.subscriberCount == 1)
        {
            // only one subscriber: query now
            subscription.nextQueryTimestamp = now;
            subscriber.nextQueryTimestamp = subscription.nextQueryTimestamp;
            nextSubscriptionIdQueue.insert(subscriptionId);
        }
        else
        {
            // sync next query with existing notification rhythms:
            // 1. select other subscriber as reference to sync with by greatest common divisor of periods
            ASSERT(subscription.firstSubscriberIndex >= 0);
            int32_t refIdx = -1;
            {
                int16_t maxGcd = 0;
                int32_t idx = subscription.firstSubscriberIndex;
                while (idx >= 0)
                {
                    const int16_t gcd = math_lib::greatestCommonDivisor(notificationPeriodMinutes, subscribers[idx].notificationPeriodMinutes);
                    if (maxGcd < gcd)
                    {
                        maxGcd = gcd;
                        refIdx = idx;
                    }
                    idx = subscribers[idx].nextSubscriberIdx;
                }
                ASSERT(refIdx >= 0);
            }

            // 2. compute next query time between now and reference subscriber that syncs the new subscriber with
            // the reference
            const QPI::DateAndTime& refQueryTimestamp = subscribers[refIdx].nextQueryTimestamp;
            const uint64_t minutesUntilRefQuery = now.durationMicrosec(refQueryTimestamp) / 60000000;
            ASSERT(minutesUntilRefQuery <= subscribers[refIdx].notificationPeriodMinutes);
            const int64_t periodsUntilRefQuery = int64_t(minutesUntilRefQuery) / notificationPeriodMinutes;
            subscriber.nextQueryTimestamp = refQueryTimestamp;
            const int64_t offsetMinutes = -periodsUntilRefQuery * notificationPeriodMinutes;
            if (offsetMinutes)
                subscriber.nextQueryTimestamp.add(0, 0, 0, 0, offsetMinutes, 0);
            ASSERT(subscriber.nextQueryTimestamp >= now);
            ASSERT(subscriber.nextQueryTimestamp <= refQueryTimestamp);
            ASSERT(subscriber.nextQueryTimestamp.durationMicrosec(now) / 60000000 <= notificationPeriodMinutes);

            // 3. update next query time of subscription if needed
            if (subscription.nextQueryTimestamp > subscriber.nextQueryTimestamp)
            {
                subscription.nextQueryTimestamp = subscriber.nextQueryTimestamp;
                nextSubscriptionIdQueue.removeFirstMatch(subscriptionId);
                nextSubscriptionIdQueue.insert(subscriptionId);
            }
        }

        // new add subscriber to linked list of all the subscription's subscribers at the correct position
        // (the list is sorted by nextQueryTimestamp, so this needs to be done after scheduling)
        addSubscriberToSubscriptionsSortedList(subscriptionId, subscriberIndex);

        // log new subscriber
        logger.logOracleSubscriber({ subscriptionId, interfaceIndex, contractIndex, notificationPeriodMillisec,
                *(uint64_t*)&subscriber.nextQueryTimestamp });

        // debug logging
#if !defined(NDEBUG) && !defined(NO_UEFI)
        CHAR16 dbgMsg[300];
        setText(dbgMsg, L"oracleEngine.startContractSubscription(), tick ");
        appendNumber(dbgMsg, system.tick, FALSE);
        appendText(dbgMsg, ", subscriptionId ");
        appendNumber(dbgMsg, subscriptionId, FALSE);
        appendText(dbgMsg, ", interfaceIndex ");
        appendNumber(dbgMsg, interfaceIndex, FALSE);
        appendText(dbgMsg, ", contractIndex ");
        appendNumber(dbgMsg, contractIndex, FALSE);
        appendText(dbgMsg, ", notificationPeriodMillisec ");
        appendNumber(dbgMsg, notificationPeriodMillisec, FALSE);
        appendText(dbgMsg, ", firstQueryTimestamp ");
        appendDateAndTime(dbgMsg, subscriber.nextQueryTimestamp);
        appendText(dbgMsg, ", now ");
        appendDateAndTime(dbgMsg, QPI::DateAndTime::now());
        addDebugMessage(dbgMsg);
#endif

        return subscriptionId;
    }

    bool stopContractSubscription(int32_t subscriptionId, uint16_t contractIndex)
    {
        // initial check of inputs
        if (subscriptionId < 0 || contractIndex >= MAX_NUMBER_OF_CONTRACTS)
            return false;

        // lock for accessing engine data
        LockGuard lockGuard(lock);

        // check that contract is subscribed for subscription
        if (subscriptionId >= usedSubscriptionSlots)
            return false;

        // Find subscriber in linked list of subscription and remove it
        OracleSubscription& subscription = subscriptions[subscriptionId];
        if (subscription.firstSubscriberIndex < 0)
            return false;
        ASSERT(subscription.firstSubscriberIndex < usedSubscriberSlots);
        OracleSubscriber* subscriber = &subscribers[subscription.firstSubscriberIndex];
        if (subscriber->contractIndex == contractIndex)
        {
            // subscriber is the first in the subscription linked list
            subscription.firstSubscriberIndex = subscriber->nextSubscriberIdx;
            if (subscription.firstSubscriberIndex >= 0)
            {
                // there is at least one additional subscriber -> update timestamp to next subscriber
                subscription.nextQueryTimestamp = subscribers[subscription.firstSubscriberIndex].nextQueryTimestamp;
                nextSubscriptionIdQueue.removeFirstMatch(subscriptionId);
                nextSubscriptionIdQueue.insert(subscriptionId);
            }
            else
            {
                // there is no subscriber for this subscription anymore -> remove subscription from queue
                subscription.nextQueryTimestamp.setInvalid();
                nextSubscriptionIdQueue.removeFirstMatch(subscriptionId);
            }
        }
        else
        {
            // find subscriber in linked list and remove it
            int32_t prevIdx = subscription.firstSubscriberIndex;
            int32_t curIdx = subscriber->nextSubscriberIdx;
            while (curIdx >= 0 && subscribers[curIdx].contractIndex != contractIndex)
            {
                prevIdx = curIdx;
                curIdx = subscribers[curIdx].nextSubscriberIdx;
            }
            if (curIdx < 0)
                return false;

            // found subscriber in curIdx -> remove it from linked list by linking previous entry to
            // to following entry
            subscriber = &subscribers[curIdx];
            subscribers[prevIdx].nextSubscriberIdx = subscriber->nextSubscriberIdx;
        }

        // Disable subscriber by invalidating nextQueryTimestamp, but keep slot occupied, because it is referenced
        // in the oracle query storage
        subscriber->nextQueryTimestamp.setInvalid();
        subscriber->nextSubscriberIdx = -1;

        ASSERT(subscription.subscriberCount >= 1);
        --subscription.subscriberCount;

        // log removed subscriber
        logger.logOracleSubscriber({ subscriptionId, subscription.interfaceIndex, contractIndex, 0, 0 });

        // debug logging
#if !defined(NDEBUG)// && !defined(NO_UEFI)
        CHAR16 dbgMsg[300];
        setText(dbgMsg, L"oracleEngine.stopContractSubscription(), tick ");
        appendNumber(dbgMsg, system.tick, FALSE);
        appendText(dbgMsg, ", subscriptionId ");
        appendNumber(dbgMsg, subscriptionId, FALSE);
        appendText(dbgMsg, ", interfaceIndex ");
        appendNumber(dbgMsg, subscription.interfaceIndex, FALSE);
        appendText(dbgMsg, ", contractIndex ");
        appendNumber(dbgMsg, contractIndex, FALSE);
        appendText(dbgMsg, ", old notificationPeriodMillisec ");
        appendNumber(dbgMsg, subscriber->notificationPeriodMinutes * 60000, FALSE);
        appendText(dbgMsg, ", now ");
        appendDateAndTime(dbgMsg, QPI::DateAndTime::now());
        addDebugMessage(dbgMsg);
#endif

        return true;
    }

    // Called once per tick by tick processor to generate oracle queries for subscriptions.
    void generateSubscriptionQueries()
    {
        QPI::DateAndTime now = QPI::DateAndTime::now();
#if !defined(NDEBUG) && !defined(NO_UEFI)
        CHAR16 dbgMsg[200];
        setText(dbgMsg, L"oracleEngine.generateSubscriptionQueries(), tick ");
        appendNumber(dbgMsg, system.tick, FALSE);
        appendText(dbgMsg, ", now ");
        appendDateAndTime(dbgMsg, now);
        addDebugMessage(dbgMsg);
#endif

        // lock for accessing engine data
        LockGuard lockGuard(lock);

        // Check the first elements of the subscription queue, that is, the next subscriptions that need to generate a
        // query. Generate a query when it is time for it now.
        int32_t subscriptionId = -1;
        while (nextSubscriptionIdQueue.peek(subscriptionId))
        {
            // Check if it is time to generate the next query
            ASSERT(subscriptionId >= 0 && subscriptionId < usedSubscriptionSlots);
            OracleSubscription& subscription = subscriptions[subscriptionId];
            QPI::DateAndTime queryTimestamp = subscription.nextQueryTimestamp;
            if (queryTimestamp > now)
                break;

#if !defined(NDEBUG) && !defined(NO_UEFI)
            setText(dbgMsg, L"oracleEngine.generateSubscriptionQueries(): subscription ");
            appendNumber(dbgMsg, subscriptionId, FALSE);
            appendText(dbgMsg, ", queryTimestamp ");
            appendDateAndTime(dbgMsg, queryTimestamp);
            addDebugMessage(dbgMsg);
#endif

            // Gather subscriber indices for this query and update their nextQueryTimestamp
            uint64_t subscriberIndicesBytes = subscription.subscriberCount * sizeof(int32_t);
            __ScopedScratchpad scratchpad(subscriberIndicesBytes, /*initZero=*/false);
            uint16_t subscriberCount = 0;
            auto* subscriberIndices = (int32_t*)scratchpad.ptr;
            while (subscription.firstSubscriberIndex >= 0 && subscriberCount < subscription.subscriberCount)
            {
                // add subscriber indices with timestamp <= queryTimestamp to query
                const int32_t subscriberIdx = subscription.firstSubscriberIndex;
                OracleSubscriber& subscriber = subscribers[subscriberIdx];
                ASSERT(subscriber.subscriptionId == subscriptionId);
                ASSERT(subscriber.nextQueryTimestamp >= queryTimestamp);
                if (subscriber.nextQueryTimestamp > queryTimestamp)
                    break;
                subscriberIndices[subscriberCount++] = subscriberIdx;

                // update timestamp, potentially skipping some periods if network was stuck for a while
                // (planned query with queryTimestamp is still generated to inform the contracts via timeout)
                do
                {
                    subscriber.nextQueryTimestamp.add(0, 0, 0, 0, subscriber.notificationPeriodMinutes, 0);
                } while (subscriber.nextQueryTimestamp < now);

#if !defined(NDEBUG) && !defined(NO_UEFI)
                setText(dbgMsg, L"oracleEngine.generateSubscriptionQueries(): -> subscriber ");
                appendNumber(dbgMsg, subscriber.contractIndex, FALSE);
                appendText(dbgMsg, " -> nextQueryTimestamp ");
                appendDateAndTime(dbgMsg, subscriber.nextQueryTimestamp);
                addDebugMessage(dbgMsg);
#endif

                // remove subscriber from the head of the list and insert it to the right place based on nextQueryTimestamp
                subscription.firstSubscriberIndex = subscriber.nextSubscriberIdx;
                addSubscriberToSubscriptionsSortedList(subscriptionId, subscriberIdx);
            }
            ASSERT(subscriberCount >= 1);
            subscriberIndicesBytes = subscriberCount * sizeof(int32_t);

            // Timeout for subscriptions is always 1 minute
            QPI::DateAndTime timeout = queryTimestamp;
            timeout.add(0, 0, 0, 0, 1, 0);

            // Generate new query ID
            const int64_t queryId = getNewNonTxQueryId();
            if (queryId < 0)
                break;

            // run common part of starting query and init query metadata
            ASSERT(subscription.interfaceIndex < OI::oracleInterfacesCount);
            const uint16_t querySize = (int16_t)OI::oracleInterfaces[subscription.interfaceIndex].querySize;
            OracleQueryMetadata* oqm = startQuery(queryId, ORACLE_QUERY_TYPE_CONTRACT_SUBSCRIPTION,
                                                  subscription.interfaceIndex, timeout,
                                                  querySize + subscriberIndicesBytes);
            if (!oqm)
                break;
            oqm->typeVar.subscription.subscriptionId = subscriptionId;
            oqm->typeVar.subscription.queryStorageOffset = queryStorageBytesUsed;
            oqm->typeVar.subscription.subscriberCount = subscriberCount;

            // copy data of initial oracle query to permanent storage and update timestamp
            ASSERT(subscription.initialQueryStorageOffset && subscription.initialQueryStorageOffset + querySize <= queryStorageBytesUsed);
            const uint8_t* initialQueryData = queryStorage + subscription.initialQueryStorageOffset;
            uint8_t* queryData = queryStorage + queryStorageBytesUsed;
            copyMem(queryData, initialQueryData, querySize);
            queryStorageBytesUsed += querySize;
            ASSERT(subscription.queryTimestampOffset + sizeof(QPI::DateAndTime) <= querySize);
            *(QPI::DateAndTime*)(queryData + subscription.queryTimestampOffset) = subscription.nextQueryTimestamp;

            // copy subscriber indices to permanent storage
            copyMem(queryStorage + queryStorageBytesUsed, subscriberIndices, subscriberIndicesBytes);
            queryStorageBytesUsed += subscriberIndicesBytes;

            // enqueue query message to oracle machine node
            if (timeout > now)
                enqueueOracleQuery(queryId, subscription.interfaceIndex, /*timeoutMillisec=*/60000, queryData, querySize);

            // log status change
            logQueryStatusChange(*oqm);

            // Debug logging
#if ENABLE_ORACLE_STATS_RECORD
            oracleStats[oqm->interfaceIndex].queryCount++;
#endif

#if !defined(NDEBUG) && !defined(NO_UEFI)
            CHAR16 dbgMsg[200];
            setText(dbgMsg, L"oracleEngine.generateSubscriptionQueries(): -> new query in tick ");
            appendNumber(dbgMsg, system.tick, FALSE);
            appendText(dbgMsg, ", queryId ");
            appendNumber(dbgMsg, queryId, FALSE);
            appendText(dbgMsg, ", interfaceIndex ");
            appendNumber(dbgMsg, oqm->interfaceIndex, FALSE);
            appendText(dbgMsg, ", timeout ");
            appendDateAndTime(dbgMsg, timeout);
            appendText(dbgMsg, ", now ");
            appendDateAndTime(dbgMsg, now);
            addDebugMessage(dbgMsg);
#endif

            // Update current subscription, statistics, and priority queue of all subscriptions
            subscription.lastPendingQueryId = queryId;
            ++subscription.generatedQueriesCount;
            ++stats.subscriptionQueries;
            ASSERT(subscription.firstSubscriberIndex >= 0);
            subscription.nextQueryTimestamp = subscribers[subscription.firstSubscriberIndex].nextQueryTimestamp;
            nextSubscriptionIdQueue.replace(subscriptionId, subscriptionId);
        }
    }

    // Refund fees if an error occurred while trying to start an oracle query
    static void refundFees(const m256i& sourcePublicKey, int64_t refundAmount)
    {
        ASSERT(refundAmount >= 0);
        increaseEnergy(sourcePublicKey, refundAmount);
        const QuTransfer quTransfer = { m256i::zero(), sourcePublicKey, refundAmount };
        logger.logQuTransfer(quTransfer);
    }

