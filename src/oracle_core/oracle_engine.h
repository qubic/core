// A subscription is defined by an oracle interface and a query with a timestamp member.
// Multiple contracts that subscribe with the same query (excluding timestamp), share the subscription.
// However, each contract (subscriber) in the same subscription may have an individual notification period and
// notification procedure, both stored in OracleSubscriber.
// For each subscription, a scheduler generates a query regularly. The query may be shared by multiple subscribers.
// Depending on the individual notification period of the contracts, some contracts are notified for the specific
// query and some are not notified.
// Subscriptions are stored in an array and the index is the subscription ID.
struct OracleSubscription
{
    /// Offset of initial query of subscription in query storage. 0 for unused slot.
    uint64_t initialQueryStorageOffset;

    /// Index of oracle interface
    uint32_t interfaceIndex;

    /// Offset of DateAndTime timestamp member variable in oracle query struct
    uint16_t queryTimestampOffset;

    /// Count of contracts that are subscribers at the moment
    uint16_t subscriberCount;

    /// Query ID of last pending query
    int64_t lastPendingQueryId;

    /// Query ID of last revealed query, for notifying with cached value on new subscription?
    int64_t lastRevealedQueryId;

    /// Timestamp for triggering next query
    QPI::DateAndTime nextQueryTimestamp;

    /// Total number of queries generated by this subscription
    uint32_t generatedQueriesCount;

    /// First element in linked list of subscribers, sorted by their nextQueryTimestamp
    int32_t firstSubscriberIndex;
};

// Subscribers are stored in an array and the index is the subscriber index.
// Subscriber indices are referenced by all subscription queries in the query storage. So even if unsubscribed,
// a subscriber index should not be reused before the query storage is cleared.
struct OracleSubscriber
{
    /// Index of corresponding subscription in subscriptions array
    int32_t subscriptionId;

    /// Index of contract that subscribed and will be notified
    uint16_t contractIndex;

    /// Number of minutes between queries that this subscriber is notified about
    uint16_t notificationPeriodMinutes;

    /// Timestamp for triggering next query that this subscriber is notified about
    QPI::DateAndTime nextQueryTimestamp;

    /// ProcId called for notifying this subscriber contract
    uint32_t notificationProcId;

    // Index of next subscriber in linked list of all subscribers with same subscription
    int32_t nextSubscriberIdx;
};

// State of received OM reply and computor commits for a single oracle query
struct OracleReplyState
{
    int64_t queryId;

    // reply data provided by own OM node
    m256i ownReplyDigest;
    uint16_t ownReplySize;
    uint8_t ownReplyData[MAX_ORACLE_REPLY_SIZE + 2];

    // track when own reply commits tx are scheduled (per own computorIdx)
    uint32_t replyCommitScheduleTick[NUMBER_OF_COMPUTORS];

    // track execution of reply commit tx of each computor
    m256i replyCommitDigests[NUMBER_OF_COMPUTORS];
    m256i replyCommitKnowledgeProofs[NUMBER_OF_COMPUTORS];
    uint32_t replyCommitTicks[NUMBER_OF_COMPUTORS];

    // aggregation of reply commits (groups that share the same digest are counted in the same histogram bin)
    uint16_t replyCommitHistogramIdx[NUMBER_OF_COMPUTORS];
    uint16_t replyCommitHistogramCount[NUMBER_OF_COMPUTORS];
    uint16_t mostCommitsHistIdx;
    uint16_t totalCommits;

    // tick for which reveal tx is scheduled or has been announced (used to reduce the number of reveal tx sent)
    uint32_t expectedRevealTxTick;
};

